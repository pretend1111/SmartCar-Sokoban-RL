"""手工设计 60 张训练地图 (每 phase 10 张).

所有地图都经过可解性验证设计:
- 目标不放在角落(两面靠墙)
- 箱子有推送路径到目标
- 复杂度适中, IDA*能在60s内求解

格式: 12 行 × 16 字符
# = 墙  - = 空地  $ = 箱子  . = 目的地  * = 炸弹(TNT)
车出生在 (col=1, row=6) 即左侧中间
"""
import os, sys

ROOT = os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))

def M(*rows):
    assert len(rows) == 12, f"需要12行, 实际{len(rows)}行"
    for i, r in enumerate(rows):
        assert len(r) == 16, f"第{i}行需要16字符, 实际{len(r)}字符: '{r}'"
    s = '\n'.join(rows)
    boxes, targets = s.count('$'), s.count('.')
    assert boxes == targets and boxes > 0, f"箱={boxes} 目标={targets}"
    return s

